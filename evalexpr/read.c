long				read_long(char **str)
{
	long		res;
	long		sgn;

	sgn = 1;
	if (**str == '-')
	{
		sgn = -1;
		(*str)++;
	}
	res = 0;
	while ('0' <= **str && **str <= '9')
	{
		res = res * 10 + ((**str - '0') % 10);
		(*str)++;
	}
	return (sgn * res);
}

void			skip_spaces(char **str)
{
	while (**str == ' ')
	{
		(*str)++;
	}
}
